package net.frozenblock.freezeframe.data;

import java.util.concurrent.CompletableFuture;
import net.fabricmc.fabric.api.datagen.v1.FabricPackOutput;
import net.fabricmc.fabric.api.datagen.v1.provider.FabricDynamicRegistryProvider;
import net.frozenblock.freezeframe.FFConstants;
import net.frozenblock.freezeframe.registry.FFRegistries;
import net.minecraft.core.HolderGetter;
import net.minecraft.core.HolderLookup;

final class FFRegistryProvider extends FabricDynamicRegistryProvider {

	FFRegistryProvider(FabricPackOutput output, CompletableFuture<HolderLookup.Provider> registriesFuture) {
		super(output, registriesFuture);
	}

	@Override
	protected void configure(HolderLookup.Provider registries, Entries entries) {
		FFConstants.log("Adding finalized special film filters to datagen", true);
		entries.addAll(asLookup(entries.getLookup(FFRegistries.SPECIAL_FILM_FILTER)));
	}

	public static <T> HolderLookup.RegistryLookup<T> asLookup(HolderGetter<T> getter) {
		return (HolderLookup.RegistryLookup<T>) getter;
	}

	@Override
	public String getName() {
		return "Freeze Frame Dynamic Registries";
	}
}
